    private void docdulieu(String mahd){
        ThanhToan_Dao ds = new ThanhToan_Dao();
        List<MonAn> list = ds.getall(mahd);
        count = 0;
        for (MonAn ma : list)
        {
            SimpleDateFormat fm = new SimpleDateFormat("dd/MM/yyyy");
            DecimalFormat dfm = new DecimalFormat("#,###đ");
            count++;
            String Ten = ma.getTenMonAn();
            String size = ma.getSize();
            int sl = ma.getsl();
            int km = ma.getGiaTien()*ma.getKhuyenMai();
            int c = (ma.getGiaTien() - km/100)*sl;
            sl = ma.getsl();
            tongtienma += c;
            String [] r = {"" + count, Ten, sl+"", size, c+""};
            jTableModel1.addRow(r);
        }
        jTable1.setModel(jTableModel1);
    }
    
