    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Date d = new Date();
        int day = 0;
        String dd = "";
        day = d.getDate();
        if (day < 10)
            dd = "0"+day;
        else dd = day + "";
        int month;
        String mm = "";
        month = d.getMonth()+1;
        if (month < 10)
            mm = "0"+month;
        else mm = month + "";
        int year;
        String yy = "";
        year = d.getYear()-100;
        if (year < 10)
            yy = "0"+year;
        else yy = year + "";
        String mphiu = "P"+dd+mm+yy;
        int tck = Integer.parseInt(jLabel17.getText());
        ThanhToan_Dao tt = new ThanhToan_Dao();
        Phieu p = new Phieu(mphiu, maHD, d, tphieu[0], tphieu[1], tphieu[2], tphieu[3], 
                tphieu[4], tphieu[5], tphieu[6], tphieu[7], tphieu[8], tck);
        if (tt.crephieu(p)) {
            Ban_DAO b = new Ban_DAO();
            b.upd("Trống", b.getmathd(maHD));
            tt.upd(d, maHD);
            JOptionPane.showMessageDialog(this, "Lưu thành công");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

