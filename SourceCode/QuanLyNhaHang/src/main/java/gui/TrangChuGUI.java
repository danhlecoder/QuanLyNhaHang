    private void themMon(){
        TaiKhoan_DAO tk = new TaiKhoan_DAO();
        TrangChu_DAO tc = new TrangChu_DAO();
        Ban_DAO b = new Ban_DAO();
        String manv = tk.getma();
        
        mahd = tc.phatsinh(manv);
        Date now = new Date();
        String makh = "";
        if (!jLabel13.getText().equals("Khách hàng")){
            makh = jLabel13.getText();
            String mab = b.getma(jLabel5.getText());
            HoaDon hd = new HoaDon(mahd, now, new KhachHang(makh), new NhanVien(manv), null, 10, null,new Ban(mab), list);
            if (tc.crehd(hd)) {
                for (MonAn e : hd.getMaMonAn()){
                    tc.crecthd(e, hd.getMaHoaDon());
                }
                JOptionPane.showMessageDialog(this, "Lưu thành công"); 
            }
        } 
        else 
            JOptionPane.showMessageDialog(this, "Chọn khách hàng!!!");
    }
