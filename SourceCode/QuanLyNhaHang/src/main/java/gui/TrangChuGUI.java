    private void paintBan(List<Ban> list){
        int x=20;
        int y=20;
        
        for (Ban ban : list) {
            JButton jp = paint1Ban(ban);
            jPanel5.add(jp, new org.netbeans.lib.awtextra.AbsoluteConstraints(x, y, 87, 87));
            jp.setBounds(x, y, 87, 87);
            x=x+117;
            if(x>672){
                x=20;
                y=y+97;
            }
            
            //jpp = new javax.swing.JPanel();
            jp.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                jLabel5.setText(ban.getten());
                jPanel6.repaint();
                jLabel13.setText("Khách Hàng");
                tongtienHD=0;
                Ban_DAO b = new Ban_DAO();
                b.fillZero();
                b.capNhapHoatDong(ban.getMaBan());
                jPanel6.removeAll();
                TrangChu_DAO ds = new TrangChu_DAO();
                List<HoaDon> list = ds.getall(ban.getMaBan());
                int count = 0;
                int z = 0;
                for (HoaDon s : list)
                { 
                    String tma = null;
                    int c = 0;
                    int sl = 0;
                    mahd = s.getMaHoaDon();
                    jLabel13.setText(s.getMaKH().getMaKH());
                    jLabel15.setText(s.getMaMonAn().size()+"");
                    for (MonAn ma : s.getMaMonAn()){
                        tma = ma.getMaMonAn();
                        sl = ma.getsl();
                        int km = ma.getGiaTien()*ma.getKhuyenMai();
                        c = ma.getGiaTien() - km/100;
                        //s.getMaMonAn().removeFirst();
                        count++;
                        JPanel jpp = paint1mon(count, tma, sl, c);
                        
                        // Code of sub-components and layout - not shown here
                        jPanel6.add(jpp, new org.netbeans.lib.awtextra.AbsoluteConstraints(10, 10+z, 670, 90));
                        z += 90;
                    }
                    
                }
            }
        });
        }
                
    }
